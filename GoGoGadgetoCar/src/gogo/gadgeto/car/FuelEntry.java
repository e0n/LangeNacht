package gogo.gadgeto.car;

public class FuelEntry {
	
	public String date;
	public String distance;
	public String costs;
	
	public FuelEntry() {
		super();
	}
	
	public FuelEntry(String date, String distance, String costs) {
		super();
		this.date = date;
		this.distance = distance;
		this.costs = costs;
	}

}
