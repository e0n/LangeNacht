package gogo.gadgeto.car;

public class FuelEntry {
	
	public String date;
	public String distance;
	public String costs;
	public String fuelid;
	
	public FuelEntry() {
	}
	
	public FuelEntry(String date, String distance, String costs, String fuelid) {
		super();
		this.date = date;
		this.distance = distance;
		this.costs = costs;
		this.fuelid = fuelid;
	}

}
