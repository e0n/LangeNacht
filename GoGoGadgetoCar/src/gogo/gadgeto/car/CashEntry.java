package gogo.gadgeto.car;

public class CashEntry {
	
	public String name;
	public String dept;
	public String mileage;
	
	public CashEntry() {
		super();
	}
	
	public CashEntry(String name, String dept, String mileage) {
		super();
		this.name = name;
		this.dept = dept;
		this.mileage = mileage;
	}

}
